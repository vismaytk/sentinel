def graceful_shutdown(signum=None, frame=None):
    """Handle graceful shutdown on SIGINT/SIGTERM."""
    print("\n  рҹӣ‘ Shutting down SENTINEL...")
    
    # Stop camera
    stop_camera()
    
    # Close database (flushes write queue)
    close_database()
    
    print("  вң… Shutdown complete")
    sys.exit(0)


def main():
    """Main entry point."""
    cfg = get_config()
    
    # Print startup banner
    print_banner()
    
    # Register signal handlers for graceful shutdown
    signal.signal(signal.SIGINT, graceful_shutdown)
    signal.signal(signal.SIGTERM, graceful_shutdown)
    atexit.register(graceful_shutdown)
    
    # Initialize database
    print("  рҹ“Ҡ Initializing database...")
    get_database()
    
    # Warmup detection models
    print("  рҹ”Ҙ Warming up detection models...")
    detector = get_detector()
    detector.warmup()
    
    # Start camera stream
    print(f"  рҹ“Ў Starting camera stream...")
    get_camera(cfg.IP_CAM_URL)
    
    # Create and run Flask app
    print(f"  рҹҡҖ Starting web server on port {cfg.PORT}...")
    app = create_app()
    
    # Run with threading enabled for SSE support
    app.run(
        host=cfg.HOST,
        port=cfg.PORT,
        debug=cfg.DEBUG,
        threaded=True,
        use_reloader=False  # Disable reloader to prevent double initialization
    )


if __name__ == "__main__":
    main()
